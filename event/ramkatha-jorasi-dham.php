<script>
  const slider = document.querySelector(".banner-slider");
  let slides = document.querySelectorAll(".banner-slide");

  const GAP = 16;
  let index = 1;
  let startX = 0;
  let currentTranslate = 0;
  let prevTranslate = 0;
  let isDragging = false;
  let autoSlide;

  /* ===== Clone Slides for Infinite Loop ===== */
  slider.appendChild(slides[0].cloneNode(true));
  slider.insertBefore(slides[slides.length - 1].cloneNode(true), slider.firstChild);
  slides = document.querySelectorAll(".banner-slide");

  function slideWidth() {
    return slides[0].offsetWidth + GAP;
  }

  function updateClasses() {
    slides.forEach(slide => slide.className = "banner-slide");

    slides[index].classList.add("active");
    slides[index - 1]?.classList.add("prev");
    slides[index + 1]?.classList.add("next");
  }

  function setPosition() {
    slider.style.transform = `translateX(${currentTranslate}px)`;
    updateClasses();
  }

  function setByIndex() {
    slider.style.transition = "transform 0.6s ease";

    const viewport = window.innerWidth;
    const slideW = slideWidth();

    // Equal left & right visibility
    const sidePeek = viewport < 768
      ? viewport * 0.07   // üì± ~7% each side
      : slideW * 0.1;    // üíª ~10% each side

    currentTranslate = -index * slideW + sidePeek;
    setPosition();
  }

  setByIndex();

  /* ===== Auto Slide ===== */
  function startAuto() {
    autoSlide = setInterval(() => {
      index++;
      setByIndex();

      if (index >= slides.length - 1) {
        setTimeout(() => {
          slider.style.transition = "none";
          index = 1;
          setByIndex();
        }, 600);
      }
    }, 3000);
  }

  function stopAuto() {
    clearInterval(autoSlide);
  }

  /* ===== Drag & Swipe ===== */
  function startDrag(e) {
    isDragging = true;
    startX = getX(e);
    prevTranslate = currentTranslate;
    slider.style.transition = "none";
    stopAuto();
  }

  function dragging(e) {
    if (!isDragging) return;
    currentTranslate = prevTranslate + (getX(e) - startX);
    setPosition();
  }

  function endDrag() {
    if (!isDragging) return;
    isDragging = false;

    const movedBy = currentTranslate - prevTranslate;

    if (movedBy < -80) index++;
    if (movedBy > 80) index--;

    setByIndex();
    startAuto();
  }

  function getX(e) {
    return e.type.includes("mouse") ? e.pageX : e.touches[0].clientX;
  }

  slider.addEventListener("mousedown", startDrag);
  slider.addEventListener("mousemove", dragging);
  slider.addEventListener("mouseup", endDrag);
  slider.addEventListener("mouseleave", endDrag);

  slider.addEventListener("touchstart", startDrag);
  slider.addEventListener("touchmove", dragging);
  slider.addEventListener("touchend", endDrag);

  window.addEventListener("resize", setByIndex);

  startAuto();
</script>



<script>
function toggleInfo(header) {
  const card = header.parentElement;
  const content = card.querySelector(".info-open-content");
  const arrow = header.querySelector(".fa-chevron-up");

  const isOpen = content.style.display === "block";

  // close
  if (isOpen) {
    content.style.display = "none";
    arrow.style.transform = "rotate(180deg)";
  } 
  // open
  else {
    content.style.display = "block";
    arrow.style.transform = "rotate(0deg)";
  }
}
</script>




